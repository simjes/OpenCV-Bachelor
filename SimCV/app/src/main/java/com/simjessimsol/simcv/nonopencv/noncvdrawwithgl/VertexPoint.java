package com.simjessimsol.simcv.nonopencv.noncvdrawwithgl;

public class VertexPoint {

    private float x;
    private float y;

    public VertexPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
